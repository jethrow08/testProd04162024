function downloadPDF1() {

    var pdfPath = 'assets/grade8pdf/Math 8-Q1-Module 1.pdf';
    
    
    var a = document.createElement('a');
    a.href = pdfPath;
    
    
    a.download = 'Math 8-Q1-Module 1.pdf';
    
    
    document.body.appendChild(a);
    
    
    a.click();
    
    
    document.body.removeChild(a);
    }
    
